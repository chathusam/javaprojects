<p>As an online business our prices change in accordance with trends, customer demand and stock availability we are, therefore, unable to refund the difference in price when an item you have purchased goes into sale, although we do appreciate how frustrating this can be.</p>
<p>You can of course return the item for a full refund and then re-order the item at its markdown price, provided it is returned in accordance with our returns policy. Please note, we cannot guarantee that another customer won't purchase this item first.</p>


